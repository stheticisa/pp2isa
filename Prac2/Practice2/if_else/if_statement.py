a = 10
b = 5

# Check if a is greater than b
if a > b:
    print("a is greater than b")

# Check if a is equal to b
if a == b:
    print("a is equal to b")

# Check if b is positive
if b > 0:
    print("b is positive")

# Check if a is even
if a % 2 == 0:
    print("a is even")

# Check if b is negative
if b < 0:
    print("b is negative")
